// 대,소문자,숫자 6~12자
export const ID_REGEX = /^[a-zA-Z][a-zA-Z0-9]{5,11}$/;

/**
 * 영문, 숫자, 안전한 특수문자 각각 1개 이상 포함, 8-20자
 * 설명:
 * (?=.*[a-z])                        : 소문자 1개 이상
 * (?=.*[0-9])                        : 숫자 1개 이상
 * (?=.*[!@#$%^&*_+=\-])              : 안전한 특수문자 1개 이상
 * [a-zA-Z0-9!@#$%^&*_+=\-]{8,20}     : 허용된 문자만, 8-20자
 * 허용 특수문자: ! @ # $ % ^ & * _ + = -
 * 차단 특수문자: ' " ` ; \ / < > ( ) [ ] { } | ~ ?
 */
export const PASSWORD_REGEX =
  /^(?=.*[a-zA-Z])(?=.*[0-9])(?=.*[!@#$%^&*_+=\-])[a-zA-Z0-9!@#$%^&*_+=\-]{8,20}$/;
